	 public function insert(array $data)
    {
        // add a timestamp
        $data['subject_added'] = date('Y-m-d H:i:s');
        $data['subject_code'] 	= $this->createReference();
		$data['subject_url']		= $this->toUrl($data['subject_name']);
		
		return parent::insert($data);
		
