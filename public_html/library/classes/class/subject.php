	 * @param query string $where
	 * @param array $data
     * @return boolean
	 */
    public function update(array $data, $where)
    {
        // add a timestamp
        $data['subject_updated'] = date('Y-m-d H:i:s');
        if(isset($data['subject_url'])) $data['subject_url'] = $this->toUrl($data['subject_name']);
		
        return parent::update($data, $where);
    }
	
